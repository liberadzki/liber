expect_is(mode(c(1,2)),"integer")
expect_error(mode(c("1","2")))
expect_error(mode(1,2))
expect_error(mode(c(1,"2")))
expect_error(mode(list(c(1,2),c(11,11))))
expect_equal(mode(c(2,2,2)),2)
expect_equal(mode(c(1:10,1)),1)
expect_equal(mode(c(2,2,5,1,4,5,1,90,5)),5)
expect_equal(mode(1),1)
expect_equal(mode(c(2.2,2.7,2.5)),2)
